    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (Vendor_Id.getSelectedItem().toString().equals("") || Vendor_Name.getText().equals("") || Vendor_Image.getIcon().equals(null) 
        || Bill_Number.getSelectedItem().toString().equals("Select") || Currency.getText().equals("") || Total_Sum.getText().equals("") 
      || Balance.getText().equals("")|| status.getSelectedItem().toString().equals("Status") || Date.getDate().toString().equals("") ||
         Paid_Amount.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please Fill All The Fields");
        }
        else 
        {
        Connection con = ModelLogin.getConnection();
        String curr = Currency.getText();
        String sum = Total_Sum.getText();
        String bal = Balance.getText();
        String pamo = Paid_Amount.getText();


                  java.util.Date date=Date.getDate();
                    java.sql.Date Dat=new java.sql.Date(date.getTime());

        try {
             ps=con.prepareStatement("INSERT INTO `generate_payment`(`date`, `totalamount`, `currency`, `status`, `billno`, `paidamount`, `balance`, `vendorid`) VALUES (?,?,?,?,?,?,?,?)");
             
        
            ps.setString(4,status.getSelectedItem().toString());
            ps.setString(5, Bill_Number.getSelectedItem().toString());
            ps.setString(3,curr);
            ps.setString(2, sum);
            ps.setString(7, bal);
            ps.setString(6, pamo);
            ps.setDate(1, Dat);
            ps.setString(8, Vendor_Id.getSelectedItem().toString());
            ps.execute();
            JOptionPane.showMessageDialog(this, "values addeed successfully ");
            Vendor_Id.setSelectedIndex(0);
            Vendor_Name.setText("");
            Vendor_Image.setIcon(null);
            Bill_Number.setSelectedIndex(0);
            Currency.setText("");
            Total_Sum.setText(""); 
            Balance.setText("");
            status.setSelectedIndex(0);
            Date.setDate(null);
            Paid_Amount.setText("");
        } catch (Exception e) {
            System.out.println(e);
        }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

