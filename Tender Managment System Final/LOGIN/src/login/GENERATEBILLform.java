    private void jComboBox12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox12ActionPerformed
        // TODO add your handling code here:
        Connection con = ModelLogin.getConnection();
        int i = 0;
        double tprice;

        try {
            ps = con.prepareStatement("SELECT quantity,price FROM `get_rates` WHERE `description` =?");
            ps.setString(1, (String) jComboBox12.getSelectedItem());
            rs = ps.executeQuery();

            while (rs.next()) {
                int p = rs.getInt("price");
                int q = rs.getInt("quantity");

                tprice = p * q;
                jTable1.setValueAt(q, i, 1);
                jTable1.setValueAt(p, i, 2);
                jTable1.setValueAt(tprice, i, 3);
                i++;

            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_jComboBox12ActionPerformed

