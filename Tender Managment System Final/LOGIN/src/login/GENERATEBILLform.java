    private void Vendor_IdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Vendor_IdActionPerformed
        // TODO add your handling code here:
        Connection con = ModelLogin.getConnection();

        try {
            ps = con.prepareStatement("SELECT `name`,image FROM `vendor` WHERE `vendorid` =?");
            ps.setString(1, (String) Vendor_Id.getSelectedItem());
            rs = ps.executeQuery();
            while (rs.next()) {
                Vendor_Name.setText(rs.getString("name"));
                byte[] img = rs.getBytes("image");
                ImageIcon image = new ImageIcon(img);
                Image im = image.getImage();
                Image myImage = im.getScaledInstance(Vendor_Image.getWidth(), Vendor_Image.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon newImage = new ImageIcon(myImage);
                Vendor_Image.setIcon(newImage);

            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_Vendor_IdActionPerformed

